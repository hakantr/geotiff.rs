    #[derive(Debug)]
    struct RecordingReader {
        data: Bytes,
        ranges: Arc<std::sync::Mutex<Vec<Range<u64>>>>,
    }

    #[async_trait]
    impl AsyncFileReader for RecordingReader {
        async fn get_bytes(&self, range: Range<u64>) -> AsyncTiffResult<Bytes> {
            self.ranges.lock().unwrap().push(range.clone());
            let start = usize::try_from(range.start)
                .unwrap_or(usize::MAX)
                .min(self.data.len());
            let end = usize::try_from(range.end)
                .unwrap_or(usize::MAX)
                .min(self.data.len());
            Ok(if start < end {
                self.data.slice(start..end)
            } else {
                Bytes::new()
            })
        }
    }

    #[tokio::test]
    async fn blocked_reader_aligns_and_reuses_blocks() {
        let ranges = Arc::new(std::sync::Mutex::new(Vec::new()));
        let inner: Arc<dyn AsyncFileReader> = Arc::new(RecordingReader {
            data: Bytes::from_static(b"0123456789"),
            ranges: ranges.clone(),
        });
        let reader = BlockedReader::new(inner, 4, 10).unwrap();
        assert_eq!(reader.get_bytes(3..7).await.unwrap().as_ref(), b"3456");
        assert_eq!(reader.get_bytes(4..6).await.unwrap().as_ref(), b"45");
        assert_eq!(&*ranges.lock().unwrap(), &[0..4, 4..8]);
    }

